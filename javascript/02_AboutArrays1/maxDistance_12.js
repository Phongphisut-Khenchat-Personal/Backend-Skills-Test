// หาค่าที่อยู่ไกลที่สุดในอาเรย์

// ฟังก์ชันเพื่อหาค่าที่อยู่ไกลที่สุดในอาเรย์
function maxDistance(arr) {
    // หาเลขที่น้อยที่สุดในอาเรย์
    let minValue = Math.min(...arr);  // ใช้ Math.min เพื่อหาค่าน้อยที่สุด
    // หาเลขที่มากที่สุดในอาเรย์
    let maxValue = Math.max(...arr);  // ใช้ Math.max เพื่อหาค่ามากที่สุด

    // คืนค่าผลลัพธ์เป็นอาเรย์ที่ประกอบด้วยค่าที่น้อยที่สุดและมากที่สุด
    return [minValue, maxValue];
}

// ตัวอย่างการใช้งาน
console.log(maxDistance([1, 5, 9, 20])); // ผลลัพธ์ที่คาดหวังคือ [1, 20]
console.log(maxDistance([1, 5, 9, 20, 30, 50])); // ผลลัพธ์ที่คาดหวังคือ [1, 50]
console.log(maxDistance([1, 5, 9, 20, 30, 50, 100])); // ผลลัพธ์ที่คาดหวังคือ [1, 100]
console.log(maxDistance([1, 5, 9, 20, 30, 50, 100, 200])); // ผลลัพธ์ที่คาดหวังคือ [1, 200]